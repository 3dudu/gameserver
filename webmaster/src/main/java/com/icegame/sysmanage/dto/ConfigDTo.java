package com.icegame.sysmanage.dto;

public class ConfigDTo {
	private String v;  //版本
	private String s;  //服务器ip
	private String u;  //热更ip
	private String p;  //支付类型
	private String r;  //审核标志
	private String l;  //登录地址
	private String pu;  //支付地址
	public String getV() {
		return v;
	}
	public void setV(String v) {
		this.v = v;
	}
	public String getS() {
		return s;
	}
	public void setS(String s) {
		this.s = s;
	}
	public String getU() {
		return u;
	}
	public void setU(String u) {
		this.u = u;
	}
	public String getP() {
		return p;
	}
	public void setP(String p) {
		this.p = p;
	}
	public String getR() {
		return r;
	}
	public void setR(String r) {
		this.r = r;
	}
	public String getL() {
		return l;
	}
	public void setL(String l) {
		this.l = l;
	}
	public String getPu() {
		return pu;
	}
	public void setPu(String pu) {
		this.pu = pu;
	}
	
	
}
